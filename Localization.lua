function CinematicCam:GetLanguage()
    local lang = GetCVar("language.2")
    if self.localization[lang] then
        return lang
    end
    return "en"
end

function CinematicCam:CC_L(key, vars)
    local lang = self:GetLanguage()
    local text = self.localization[lang][key] or key

    if vars then
        for k, v in pairs(vars) do
            text = text:gsub("{" .. k .. "}", tostring(v))
        end
    end

    return text
end
